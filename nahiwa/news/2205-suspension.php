<h4>『ナ･ヒヴァヒヴァ･ハワイ2022』の<br class="only_sp">開催中止について</h4>
</div>
<div class="table_wrap">
<p>
　平素から『フェスティバル ナ･ヒヴァヒヴァ･ハワイ』を応援いただき、誠にありがとうございます。<br><br>

　毎年、ハワイ島ヒロで行われている『メリー・モナーク・フェスティバル』は、今年、新型コロナウイルスの感染拡大を防ぐため、チケットの一般発売を行わず、出場ハーラウやスポンサー関係者に観客を限定して開催されました。<br>
　世界的なコロナウイルスワクチンの接種促進により、ハワイ州でも徐々に観光客が戻りつつありますが、未だすべての制限が撤廃されたわけではありません。<br>
　こうした状況の中で、100人を超える関係者が来日して公演を行う『フェスティバル　ナ・ヒヴァヒヴァ・ハワイ』の今秋開催を、現時点で決断することは大変難しいのが実情です。また、開催決定を引き延ばし、結果的に中止となった場合、ハワイの多くの出演者はもちろん、本公演に期待をお寄せいただいているファンの皆様にも多大なご迷惑をおかけすることになります。<br>
　ついては、極めて残念ではありますが、昨年に続き、今年も本公演を中止させていただくことを決定いたしました。開催を心待ちにしてくださっている皆様には心よりお詫び申し上げます。
<br><br>
　今後も新型コロナウイルス感染症の感染状況などを注視しながら、来秋には改めて『ナ･ヒヴァヒヴァ･ハワイ2023』が開催できるよう、引き続き準備して参ります。今後ともご支援賜りますようお願い申し上げます。
</p><br>
